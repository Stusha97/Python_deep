from random import randint
LOWER_LIMIT = 0
UPPER_LIMIT = 1000
randintnum = randint(LOWER_LIMIT, UPPER_LIMIT)
TRY_LIMIT = 10
number_try = 0
ONE = 1

print('У вас есть 10 попыток угадать число, загаданное программой.')
while number_try < TRY_LIMIT:
    num = int(input('Введите число от 0 до 1000: '))
    if num == randintnum:
        print ('Бинго, число угадано!')
        break
    else:
        if num > randintnum :
            print('Загаданное число меньше')
        else:
            print ('Загаданное число больше')
        number_try += ONE
        print (f'Осталось {TRY_LIMIT - number_try} попыток ')
else:   
    print (f'Количество попыток исчерпано, конец игры. Загаданное число = {randintnum}')